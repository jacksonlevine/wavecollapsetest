pub struct JModel {
    pub verts: Vec<f32>,
    pub uvs: Vec<f32>,

    pub vao: GLuint,
    pub vbo: GLuint,
    pub uvbo: GLuint,
}

#[derive(IntoPrimitive, Clone, Copy)]
#[repr(usize)]
pub enum JModelIndex {
    TestCraftingTable = 0,
    TestTreeBush = 1,
    PlayerModel = 2
}

impl JModel {
    pub fn new(verts: Vec<f32>, uvs: Vec<f32>) -> Self {
        let mut vbo: GLuint = 0;
        let mut uvbo: GLuint = 0;
        let mut vao: GLuint = 0;
        unsafe {
            gl::CreateVertexArrays(1, &mut vao);
            gl::CreateBuffers(1, &mut vbo);
            gl::CreateBuffers(1, &mut uvbo);

            gl::BindVertexArray(vao);
        }
        //bind_old_geometry(vbo, uvbo, &verts, &uvs, shader);
        Self {
            verts,
            uvs,
            vao,
            vbo,
            uvbo
        }
    }

   
}




